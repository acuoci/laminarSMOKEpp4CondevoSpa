Info<< "Reading solid thermophysical properties\n" << endl;
autoPtr<solidThermo> pSolidThermo(solidThermo::New(solidMesh));
solidThermo& solidThermo = pSolidThermo();

volScalarField solidRho
(
	IOobject
	(
		"solidRho",
		runTime.timeName(),
		solidMesh
	),
	solidThermo.rho()
);

volScalarField solidKappa
(
	IOobject
	(
		"solidKappa",
		runTime.timeName(),
		solidMesh
	),
	solidThermo.kappa()
);

Info<< "    Adding to fvModelsSolid\n" << endl;
Foam::fvModels& fvModelsSolid(Foam::fvModels::New(solidMesh));
if (!fvModelsSolid.PtrListDictionary<fvModel>::size())
{
    Info << "No fvModels present for solid phase" << endl;
}

Info<< "    Adding fvConstraintsSolid\n" << endl;
Foam::fvConstraints& fvConstraintsSolid(Foam::fvConstraints::New(solidMesh));

if (!fvConstraintsSolid.PtrListDictionary<fvConstraint>::size())
{
    Info << "No fvConstraints present for solid phase" << endl;
}

